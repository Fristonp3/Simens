static uint8_t s_framebuffer[SSD1306_FB_SIZE];
static char s_shadow[BSP_OLED_LINE_COUNT][BSP_OLED_LINE_WIDTH + 1U];

static void oled_send_cmd(uint8_t cmd)
{
    uint8_t buf[2] = {SSD1306_CMD, cmd};
    board_i2c_oled_write(BOARD_I2C_OLED_ADDRESS, buf, 2U);
}

static void oled_send_data_bulk(const uint8_t *data, uint16_t length)
{
    uint8_t buf[129];
    uint16_t pos = 0U;

    while (pos < length) {
        uint16_t chunk = length - pos;
        if (chunk > 128U) { chunk = 128U; }
        buf[0] = SSD1306_DATA;
        memcpy(&buf[1], &data[pos], chunk);
        board_i2c_oled_write(BOARD_I2C_OLED_ADDRESS, buf, (uint16_t)(chunk + 1U));
        pos += chunk;
    }
}

static void oled_draw_char(uint8_t col, uint8_t page, char ch)
{
    uint8_t glyph_idx;
    uint8_t i;

    if (ch < ' ' || ch > '~') { ch = ' '; }
    glyph_idx = (uint8_t)(ch - ' ');

    for (i = 0U; i < 6U && (col + i) < SSD1306_WIDTH; i++) {
        s_framebuffer[page * SSD1306_WIDTH + col + i] = s_font6x8[glyph_idx][i];
    }
}

void bsp_oled_init(void)
{
    board_i2c_oled_init();

    oled_send_cmd(SSD1306_DISPLAY_OFF);
    oled_send_cmd(SSD1306_SET_MUX_RATIO);   oled_send_cmd(0x3FU);
    oled_send_cmd(SSD1306_SET_DISP_OFFSET); oled_send_cmd(0x00U);
    oled_send_cmd(SSD1306_SET_START_LINE);
    oled_send_cmd(SSD1306_SEG_REMAP);
    oled_send_cmd(SSD1306_COM_SCAN_DEC);
    oled_send_cmd(SSD1306_COM_PINS);        oled_send_cmd(0x12U);
    oled_send_cmd(SSD1306_CONTRAST);        oled_send_cmd(0x7FU);
    oled_send_cmd(SSD1306_ENTIRE_ON);
    oled_send_cmd(SSD1306_NORMAL_DISP);
    oled_send_cmd(SSD1306_CLOCK_DIV);       oled_send_cmd(0x80U);
    oled_send_cmd(SSD1306_CHARGE_PUMP);     oled_send_cmd(0x14U);
    oled_send_cmd(SSD1306_MEMORY_MODE);     oled_send_cmd(0x00U);
    oled_send_cmd(SSD1306_COL_ADDR);        oled_send_cmd(0x00U); oled_send_cmd(127U);
    oled_send_cmd(SSD1306_PAGE_ADDR);       oled_send_cmd(0x00U); oled_send_cmd(7U);
    oled_send_cmd(SSD1306_PRECHARGE);       oled_send_cmd(0xF1U);
    oled_send_cmd(SSD1306_VCOM_DESELECT);   oled_send_cmd(0x40U);
    oled_send_cmd(SSD1306_DISPLAY_ON);

    bsp_oled_clear();
    memset(s_shadow, 0, sizeof(s_shadow));
}

void bsp_oled_clear(void)
{
    uint8_t line;

    memset(s_framebuffer, 0x00, SSD1306_FB_SIZE);

    for (line = 0U; line < BSP_OLED_LINE_COUNT; ++line) {
        memset(s_shadow[line], ' ', BSP_OLED_LINE_WIDTH);
        s_shadow[line][BSP_OLED_LINE_WIDTH] = '\0';
    }
}

void bsp_oled_write_line(uint8_t line, const char *text)
{
    uint8_t col;
    size_t len;

    if (line >= BSP_OLED_LINE_COUNT) { return; }

    len = strlen(text);
    if (len > BSP_OLED_LINE_WIDTH) { len = BSP_OLED_LINE_WIDTH; }

    for (col = 0U; col < BSP_OLED_LINE_WIDTH; col++) {
        char ch = (col < len) ? text[col] : ' ';
        oled_draw_char(col * 6U + 1U, line * 2U, ch);
        oled_draw_char(col * 6U + 1U, line * 2U + 1U, ch);
    }
}

void bsp_oled_refresh(void)
{
    oled_send_data_bulk(s_framebuffer, SSD1306_FB_SIZE);
}
